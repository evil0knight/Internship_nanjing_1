#ifndef KEY_H
#define KEY_H

#include "../Drivers/timer.h"

/*-------------------------------------------------
 * 配对按键引脚（PB7，低电平有效，带上拉）
 -------------------------------------------------*/
#define KEY_PAIR_PIN        PB7
#define KEY_LONG_PRESS_MS   2000u   /* 长按判定阈值：2秒 */
#define KEY_DEBOUNCE_MS     20u     /* 消抖时间 */

/*-------------------------------------------------
 * 按键事件（扫描函数每次主循环调用一次返回）
 -------------------------------------------------*/
typedef enum {
    KEY_EVENT_NONE        = 0,
    KEY_EVENT_SHORT_PRESS,      /* 短按松开后产生 */
    KEY_EVENT_LONG_PRESS        /* 持续满 2 秒后产生 */
} KeyEvent;

/*-------------------------------------------------
 * 接口声明
 -------------------------------------------------*/
void Key_Init(void);

#endif /* KEY_H */
